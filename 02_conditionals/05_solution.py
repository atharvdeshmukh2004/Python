"""
Weather Activity Suggestion
Suggest an activity based on weather(e.g Sunny - Go for walk ,
Rainy - Read a book , Snowy - Make a snowman)

"""
weather = input("How is  the weather?")

if weather == "Sunny":
    print("Let`s Go for walk")

elif weather == "Rainy":
    print("Read a book")

elif weather == "Snowy":
    print("Build a snowman")        